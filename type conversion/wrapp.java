class wrapp
{
	public static void main(String args[])
	{
		Integer a=5;   //autoboxing
		int b=a+5;    //autounboxing
		System.out.println("a:"+a);
		System.out.println("b:"+b);
	}
}
//wrapper class concept in type conversion
/*  Integer is a wrapper class
	int is a primitive data type
	autoboxing:value is stored in wrapper class
	autounboxing:storing wrapper class value to primitive data type*/